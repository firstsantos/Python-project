# O while é o tipo de looping, usado para repetir um bloco de código, enquanto uma condição for verdadeira, se algum momento a condição for falsa o programa para



print("Exemplo de while")

computador = 0

while computador < 5:
# while True:
    print("Contagem:", computador)
    computador += 1
    if computador == 4:
        break





print("Programa finalizado")


